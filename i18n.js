function getLang() {
  return document.documentElement.dataset.siteLang === "en" ? "en" : "zh";
}

function format(template, params = {}) {
  return String(template).replace(/\{(\w+)\}/g, (_, key) =>
    params[key] !== undefined && params[key] !== null ? String(params[key]) : ""
  );
}

function t(key, params) {
  const lang = getLang();
  const value = messages[lang]?.[key] ?? messages.zh[key] ?? key;
  return params ? format(value, params) : value;
}

function jobLabel(jobValue) {
  const key = jobLabelKeys[jobValue];
  return key ? t(key) : jobValue;
}

function sectionLabel(sectionId, fallbackTitle = "") {
  const key = sectionLabelKeys[sectionId] || sectionLabelByTitle[fallbackTitle];
  return key ? t(key) : fallbackTitle || sectionId;
}

function sectionDescription(sectionId, fallbackDescription = "") {
  const key = sectionDescKeys[sectionId] || sectionDescByText[fallbackDescription];
  return key ? t(key) : fallbackDescription;
}

function applyDom(root = document) {
  root.querySelectorAll("[data-i18n]").forEach((el) => {
    el.textContent = t(el.dataset.i18n);
  });
  root.querySelectorAll("[data-i18n-html]").forEach((el) => {
    el.innerHTML = t(el.dataset.i18nHtml);
  });
  root.querySelectorAll("[data-i18n-placeholder]").forEach((el) => {
    el.placeholder = t(el.dataset.i18nPlaceholder);
  });
  root.querySelectorAll("[data-i18n-title]").forEach((el) => {
    el.title = t(el.dataset.i18nTitle);
  });
  root.querySelectorAll("[data-i18n-aria]").forEach((el) => {
    el.setAttribute("aria-label", t(el.dataset.i18nAria));
  });
  root.querySelectorAll("option[data-i18n]").forEach((el) => {
    el.textContent = t(el.dataset.i18n);
  });
  root.querySelectorAll("[data-i18n-page-title]").forEach((el) => {
    document.title = t(el.dataset.i18nPageTitle);
  });
}

function updateLangToggle() {
  const toggle = document.querySelector("#langToggle");
  if (!toggle) return;
  const lang = getLang();
  const nextIsEn = lang !== "en";
  const label = toggle.querySelector(".lang-toggle-label");
  if (label) label.textContent = nextIsEn ? t("lang.label.en") : t("lang.label.zh");
  const ariaKey = nextIsEn ? "lang.switchToEn" : "lang.switchToZh";
  toggle.setAttribute("aria-label", t(ariaKey));
  toggle.setAttribute("title", t(ariaKey));
}

function applyDocumentLang() {
  document.documentElement.lang = getLang() === "en" ? "en" : "zh-CN";
}

function setLang(lang, persist = true) {
  const nextLang = lang === "en" ? "en" : "zh";
  document.documentElement.dataset.siteLang = nextLang;
  if (persist) {
    try {
      localStorage.setItem(STORAGE_KEY, nextLang);
    } catch (error) {
      // ignore
    }
  }
  applyDocumentLang();
  applyDom();
  updateLangToggle();
  if (window.Theme?.updateToggleMeta) {
    window.Theme.updateToggleMeta(window.Theme.getTheme());
  }
  window.dispatchEvent(new CustomEvent("site-lang-change", { detail: { lang: nextLang } }));
}

function toggleLang() {
  setLang(getLang() === "en" ? "zh" : "en");
}

function initLang() {
  applyDocumentLang();
  applyDom();
  updateLangToggle();
}

document.querySelector("#langToggle")?.addEventListener("click", toggleLang);

window.I18n = { STORAGE_KEY, getLang, t, jobLabel, sectionLabel, sectionDescription, setLang, toggleLang, applyDom, updateLangToggle };

initLang();
